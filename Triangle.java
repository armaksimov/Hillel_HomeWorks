public class Triangle {
    int X1;
    int X2;
    int X3;
    int Y1;
    int Y2;
    int Y3;
    public Triangle(int x1, int y1, int x2, int y2, int x3, int y3){
        this.X1 = x1;
        this.Y1 = y1;
        this.X2 = x2;
        this.Y2 = y2;
        this.X3 = x3;
        this.Y3 = y3;
    }
}
